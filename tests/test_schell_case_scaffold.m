assert(params.isotherm.model_name == "competitive_temperature_dependent_sips", ...
    "test_schell_case_scaffold:wrongIsotherm", ...
    "Schell scaffold must preserve the source Sips isotherm name.");
assert(params.isotherm.selector == 7, ...
    "test_schell_case_scaffold:wrongNestedIsothermSelector", ...
    "Schell scaffold must record the optional core Sips selector.");
assert(params.isotherm.core_integration_status == ...
    "ready_optional_nondefault_core_sips", ...
    "test_schell_case_scaffold:missingSipsStatus", ...
    "Schell scaffold must mark Sips core integration as ready.");
assert(params.isotherm.do_not_substitute == ...
    "native_extended_langmuir_freundlich_is_not_schell_sips", ...
    "test_schell_case_scaffold:sipsSubstitutionRisk", ...
    "Schell scaffold must not treat native extended Langmuir-Freundlich as Schell Sips.");
assertSchellSipsArrays(params, sourcePack.isotherm.parameters);

assert(endsWith(scaffold.summary.expected_output_path, ...
    fullfile("validation", "reports", "schell_2013", "health", ...
    "schell_20bar_tads40_performance_central_summary.json")), ...
    "test_schell_case_scaffold:wrongSummaryPath", ...
    "Schell scaffold must expose the expected health summary path.");

fprintf("Tier 3 sanity passed: Schell case scaffold is traceable and inspectable.\n");

function assertClose(actual, expected, tolerance, context)
    assert(isnumeric(actual), ...
        "test_schell_case_scaffold:expectedNumeric", ...
        "Expected numeric value at %s.", context);
    actual = double(actual);
    expected = double(expected);
    assert(isequal(size(actual), size(expected)), ...
        "test_schell_case_scaffold:sizeMismatch", ...
        "%s expected size %s, got %s.", context, ...
        mat2str(size(expected)), mat2str(size(actual)));
    if any(abs(actual - expected) > tolerance, "all")
        error("test_schell_case_scaffold:numericMismatch", ...
            "%s expected %s, got %s.", context, ...
            mat2str(expected, 16), mat2str(actual, 16));
    end
end

function assertSchellSipsArrays(params, isoParams)
    assertClose(params.schellSipsNInfA_molPerKg, ...
        [isoParams.CO2.a, isoParams.H2.a], 1e-12, ...
        "schellSipsNInfA_molPerKg");
    assertClose(params.schellSipsNInfB_JPerMol, ...
        [isoParams.CO2.b, isoParams.H2.b], 1e-12, ...
        "schellSipsNInfB_JPerMol");
    assertClose(params.schellSipsAffA_invPa, ...
        [isoParams.CO2.A, isoParams.H2.A], 1e-20, ...
        "schellSipsAffA_invPa");
    assertClose(params.schellSipsAffB_JPerMol, ...
        [isoParams.CO2.B, isoParams.H2.B], 1e-12, ...
        "schellSipsAffB_JPerMol");
    assertClose(params.schellSipsAlpha, ...
        [isoParams.CO2.alpha, isoParams.H2.alpha], 1e-12, ...
        "schellSipsAlpha");
    assertClose(params.schellSipsBeta_invK, ...
        [isoParams.CO2.beta, isoParams.H2.beta], 1e-12, ...
        "schellSipsBeta_invK");
    assertClose(params.schellSipsSref, ...
        [isoParams.CO2.sref, isoParams.H2.sref], 1e-12, ...
        "schellSipsSref");
    assertClose(params.schellSipsTref_K, ...
        [isoParams.CO2.Tref, isoParams.H2.Tref], 1e-12, ...
        "schellSipsTref_K");
end
